def generateMatchups(types: tuple) -> dict:

    res: dict = {"4x": [], "2x": [], "1x": [], "0.5x": [], "0.25x": [], "0x": []}

    for ot in range(0, 17):
        effect: int = 1
        for dt in types:
            if dt == "None":
                continue
            effect *= TYPECHART[ot][IDS[dt]]
        rid = list(IDS.keys())[list(IDS.values()).index(ot)]
        if effect == 4.0:
            res["4x"].append(rid)
        elif effect == 2.0:
            res["2x"].append(rid)
        elif effect == 1.0:
            res["1x"].append(rid)
        elif effect == 0.5:
            res["0.5x"].append(rid)
        elif effect == 0.25:
            res["0.25x"].append(rid)
        elif effect == 0.0:
            res["0x"].append(rid)

    return res
    
